// Enhanced helper function to render clickable badge with external links
export const renderTechBadge = (badge, includeExternalLink = true, additionalClasses = "") => {
    const badgeElement = (
        <img
            src={badge.url}
            alt={badge.alt}
            className={`h-6 border border-gray-700 transition-transform hover:scale-105 ${additionalClasses}`}
        />
    );

    if (includeExternalLink && technologyLinks[badge.name]) {
        return (
            <a
                key={badge.name}
                href={technologyLinks[badge.name]}
                target="_blank"
                rel="noopener noreferrer"
                className="inline-block"
                title={`Learn more about ${badge.name}`}
            >
                {badgeElement}
            </a>
        );
    }

    return (
        <span key={badge.name} className="inline-block">
            {badgeElement}
        </span>
    );
};

// Helper function to render clickable badge images
export const renderClickableBadge = (badge, linkUrl = null, additionalClasses = "") => {
    const badgeElement = (
        <img
            key={badge.name}
            src={badge.url}
            alt={badge.alt}
            className={`h-6 border border-gray-700 transition-transform hover:scale-105 ${additionalClasses}`}
        />
    );

    if (linkUrl) {
        return (
            <a
                key={badge.name}
                href={linkUrl}
                target="_blank"
                rel="noopener noreferrer"
                className="inline-block"
            >
                {badgeElement}
            </a>
        );
    }

    return badgeElement;
};

// Helper function to render badge images
export const renderBadge = (badge, additionalClasses = "") => {
    return (
        <img
            key={badge.name}
            src={badge.url}
            alt={badge.alt}
            className={`h-6 border border-gray-700 ${additionalClasses}`}
        />
    );
};

// Helper function to render multiple badges
export const renderBadges = (badges, additionalClasses = "") => {
    return badges.map(badge => renderBadge(badge, additionalClasses));
};
